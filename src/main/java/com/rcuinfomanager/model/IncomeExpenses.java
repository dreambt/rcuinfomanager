package com.rcuinfomanager.model;

public class IncomeExpenses {
    private String fmAllIncome;
    private String fmExpenses;
    private String incomeSrc;
    private String perIncome;
    private String fmOtherMemberIn;
    private String fmExpensesProj;
    private String fmInOutRatio;

    public String getFmAllIncome() {
        return fmAllIncome;
    }

    public void setFmAllIncome(String fmAllIncome) {
        this.fmAllIncome = fmAllIncome;
    }

    public String getFmExpenses() {
        return fmExpenses;
    }

    public void setFmExpenses(String fmExpenses) {
        this.fmExpenses = fmExpenses;
    }

    public String getIncomeSrc() {
        return incomeSrc;
    }

    public void setIncomeSrc(String incomeSrc) {
        this.incomeSrc = incomeSrc;
    }

    public String getPerIncome() {
        return perIncome;
    }

    public void setPerIncome(String perIncome) {
        this.perIncome = perIncome;
    }

    public String getFmOtherMemberIn() {
        return fmOtherMemberIn;
    }

    public void setFmOtherMemberIn(String fmOtherMemberIn) {
        this.fmOtherMemberIn = fmOtherMemberIn;
    }

    public String getFmExpensesProj() {
        return fmExpensesProj;
    }

    public void setFmExpensesProj(String fmExpensesProj) {
        this.fmExpensesProj = fmExpensesProj;
    }

    public String getFmInOutRatio() {
        return fmInOutRatio;
    }

    public void setFmInOutRatio(String fmInOutRatio) {
        this.fmInOutRatio = fmInOutRatio;
    }
}
