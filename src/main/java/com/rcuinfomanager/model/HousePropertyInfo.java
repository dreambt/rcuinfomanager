package com.rcuinfomanager.model;

public class HousePropertyInfo {
    private String nature;
    private String purpose;
    private String site;
    private String structure;
    private int floorNum;
    private String usedSitu;
    private String houseArea;
    private String houseWorth;
    private int isInstallment;
    private String hasCredentials;
    private String note1;

    public String getNature() {
        return nature;
    }

    public void setNature(String nature) {
        this.nature = nature;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }

    public String getStructure() {
        return structure;
    }

    public void setStructure(String structure) {
        this.structure = structure;
    }

    public int getFloorNum() {
        return floorNum;
    }

    public void setFloorNum(int floorNum) {
        this.floorNum = floorNum;
    }

    public String getUsedSitu() {
        return usedSitu;
    }

    public void setUsedSitu(String usedSitu) {
        this.usedSitu = usedSitu;
    }

    public String getHouseArea() {
        return houseArea;
    }

    public void setHouseArea(String houseArea) {
        this.houseArea = houseArea;
    }

    public String getHouseWorth() {
        return houseWorth;
    }

    public void setHouseWorth(String houseWorth) {
        this.houseWorth = houseWorth;
    }

    public int getInstallment() {
        return isInstallment;
    }

    public void setInstallment(int installment) {
        isInstallment = installment;
    }

    public String getHasCredentials() {
        return hasCredentials;
    }

    public void setHasCredentials(String hasCredentials) {
        this.hasCredentials = hasCredentials;
    }

    public String getNote1() {
        return note1;
    }

    public void setNote1(String note1) {
        this.note1 = note1;
    }
}
